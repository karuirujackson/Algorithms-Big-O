//BIG-O NOTATION - SPACE/ TIME COMPLEXITY
// 1. Constant ==> O(1) - Calculation not dependent on the input size.
// 2. Linear ==> O(n) - 1 Loop only. As value of input increases the number of times the loop executes increases.
// 3. Quadratic ==> O(n^2) - 2 Nested Loops
// 4. Cubic ==> O(n^3) - 3 Nested Loops
// 5. Logarithmic ==> O(logn) ==> Input sizes reduces by half every iteration